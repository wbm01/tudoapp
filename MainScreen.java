public void decoracaoTableTasks(){
    jTableTasks.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 14));
    jTableTasks.getTableHeader().setBackground(new Color(0,102,102));
    jTableTasks.getTableHeader().setForeground(new Color(255,255,255));
    
    jTableTasks.getColumnModel().getColumn(2).setCellRenderer(new DeadLineColumnCellRederer());
    
    jTableTasks.getColumnModel().getColumn(4).setCellRenderer(new ButtonColumnCellRederer("mudar"));
    jTableTasks.getColumnModel().getColumn(5).setCellRenderer(new ButtonColumnCellRederer("deletar"));
    
    //Incrementando ordenador da tabela
    jTableTasks.setAutoCreateRowSorter(true);
    
    jTableTasks.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int rowIndex = jTableTasks.rowAtPoint(evt.getPoint());
                int columnIndex = jTableTasks.columnAtPoint(evt.getPoint());

                if (columnIndex == 3) {
                    Tarefa tarefa = (Tarefa) ttm.getTarefas().get(rowIndex);
                    ct.atualizar(tarefa);
                }
            }
        });
}
