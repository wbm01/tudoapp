    private void jTableTasksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTasksMouseClicked
        int rowIndex = jTableTasks.rowAtPoint(evt.getPoint());
        int columnIndex = jTableTasks.columnAtPoint(evt.getPoint());
        Tarefa tarefa = ttm.getTarefas().get(rowIndex);

        switch(columnIndex){

            case 3:
            ct.atualizar(tarefa);
            ttm.isCellEditable(rowIndex, columnIndex);

            break;

            case 4:
                
            TaskDialogScreen tds = new TaskDialogScreen(this, rootPaneCheckingEnabled);
            tds.setVisible(true);
            
            
            
            
            
            
            
           
            
            break;

            case 5:
            ct.deletarId(tarefa.getId());
            ttm.getTarefas().remove(tarefa);

            int projectIndex = jList1.getSelectedIndex();
            Projeto projeto = (Projeto) pm.get(projectIndex);
            loadTarefas(projeto.getId());
            break;
        }
    }//GEN-LAST:event_jTableTasksMouseClicked

